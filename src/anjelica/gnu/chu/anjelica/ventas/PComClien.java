    public void mostrarComentarios(int cliCodi) throws SQLException
    {
      jtCom.removeAllDatos();
      if (!getSqlComent(dt, cliCodi))
          return;
      do
      {      
        ArrayList v = new ArrayList();
        v.add(dt.getFecha("cpv_fecha","dd-MM-yyyy"));
        v.add(dt.getString("cpv_come"));
        jtCom.addLinea(v);
      } while (dt.next());
      
    }
     boolean getSqlComent(DatosTabla dt, int cliCodi) throws SQLException
    {
       String s="SELECT * FROM compedven WHERE cli_codi = "+cliCodi+
        " ORDER BY cpv_fecha desc";
       return dt.select(s);
    }
