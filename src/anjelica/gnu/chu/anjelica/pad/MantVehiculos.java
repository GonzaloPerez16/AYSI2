  private boolean lockRegistro() throws UnknownHostException, SQLException, ParseException {
        if (!setBloqueo(dtAdd, "vehiculos", veh_codiE.getText())) {
            msgBox(msgBloqueo);
            nav.pulsado = navegador.NINGUNO;
            activaTodo();
            dtAdd.getConexion().rollback();
            return false;
        }
        if (!dtAdd.select("SELECT * FROM vehiculos WHERE veh_codi = " + veh_codiE.getText() , true)) {
            mensajeErr("Registro ha sido borrado");
            resetBloqueo(dtAdd, "vehiculos", veh_codiE.getText());
            activaTodo();
            mensaje("");
            return false;
        }
        return true;
    }
