    /**
     * Devuelve en el datostabla mandado el registro que indica el vehiculo mandado
     * @param vehCodi
     * @param dt
     * @return true si encuentra el registro.
     * @throws SQLException 
     */
    public static boolean getRegistro(int vehCodi, DatosTabla dt) throws SQLException
    {
        return dt.select("select * from vehiculos where veh_codi = "+vehCodi);
    }
    
   
    public static String getNombreVehiculo(int vehCodi, DatosTabla dt) throws SQLException
    {
        if (! dt.select("select * from vehiculos where veh_codi = "+vehCodi))
            return null;
        return dt.getString("veh_nomb");
    }
 
