    public void ej_query1(){
     Component c;
     if ( (c = Pprinc.getErrorConf()) != null)
     {
        c.requestFocus();
        mensaje("Error en Criterios de busqueda");
        return;
     }
   

    ArrayList v = new ArrayList();
    v.add(veh_codiE.getStrQuery());
    v.add(veh_nombE.getStrQuery());
    v.add(veh_marcaE.getStrQuery());
    v.add(veh_modeloE.getStrQuery());
    v.add(veh_matriE.getStrQuery());
    v.add(veh_anocomE.getStrQuery());
    v.add(veh_kilomeE.getStrQuery());
    

    s = "SELECT * FROM vehiculos ";
    s = creaWhere(s, v,true);
    s+=" ORDER BY veh_codi ";
    Pprinc.setQuery(false);
    
    try
    {
      if (!dtCons.select(s))
      {
        mensaje("");
        mensajeErr("No encontrados Registros para estos criterios");
        rgSelect();
        activaTodo();
        verDatos();
        return;
      }
      mensaje("");
      strSql = s;
      activaTodo();
      rgSelect();
      verDatos();
      mensajeErr("Nuevos registros selecionados");
    }
    catch (Exception ex)
    {
      fatalError("Error al buscar Representantes: ", ex);
    }

 }
