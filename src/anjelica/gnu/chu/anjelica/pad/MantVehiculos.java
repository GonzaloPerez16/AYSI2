    void verDatos()
    {
        try
        {
            if (dtCons.getNOREG())
                return;
            if (! getRegistro(dtCons.getInt("veh_codi"),dtStat))
            {
                Pprinc.resetTexto();
                veh_codiE.setValorInt(dtCons.getInt("veh_codi"));
                msgBox("Datos de Vehiculo: "+veh_codiE.getValorInt()+" No encontrados");
                return;
            }
            veh_codiE.setValorInt(dtStat.getInt("veh_codi"));
            veh_nombE.setText(dtStat.getString("veh_nomb"));
            veh_marcaE.setText(dtStat.getString("veh_marca"));            
            veh_modeloE.setText(dtStat.getString("veh_modelo"));            
            veh_matriE.setText(dtStat.getString("veh_matri"));
            veh_anocomE.setText(dtStat.getString("veh_anocom"));
            veh_kilomeE.setText(dtStat.getString("veh_kilome"));
        } catch (SQLException k)
        {
            Error("Error al ver datos de vehiculos",k);
        }
    }
